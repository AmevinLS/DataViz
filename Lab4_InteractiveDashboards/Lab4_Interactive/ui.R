library(plotly)
library(shiny)
library(shinydashboard)

shinyUI(dashboardPage(
    dashboardHeader(title="Pokemon Stuff!"),
    dashboardSidebar(
        sidebarMenu(
            menuItem("Normal", tabName="normal"),
            menuItem("Generations Histogram", tabName="genHist"),
            menuItem("DataTable", tabName="datatable"),
            menuItem("Compare types", tabName="typeComparer")
        )
    ),
    dashboardBody(
    # includeCSS("www/darkly.min.css"),
    includeCSS("www/custom.css"),
    tags$style(
        HTML('
            .dataTables_wrapper {
            width: 100% !important;
            max-width: none !important;
            }
        ')  
    ),
    
    tabItems(
        tabItem(tabName="normal",
    
            # Sidebar with a slider input for number of bins
            sidebarLayout(
                  sidebarPanel(
                      sliderInput("mean",
                                  "mean:",
                                  min = 1,
                                  max = 10,
                                  value = 5,
                                  step = 0.01),
                      sliderInput("std",
                                  "std:",
                                  min=0.1,
                                  max=5,
                                  value=1,
                                  step=0.01)
                  ),
                  mainPanel(
                      plotOutput("distPlot")
                  )
              )
        ),
        tabItem(tabName="genHist",
             sidebarLayout(
                 sidebarPanel(
                     checkboxGroupInput("genPicker", "Generations:",
                                        c("I"="I",
                                          "II"="II",
                                          "III"="III",
                                          "IV"="IV",
                                          "V"="V",
                                          "VI"="VI",
                                          "VII"="VII",
                                          "VIII"="VIII"))
                 ),
                 mainPanel(
                     plotOutput("histPlot")
                 )
             )
        ),
        tabItem(tabName="datatable",
            fluidRow(
                box(
                    width=8,
                    # plotOutput("pokeScatter", brush="scatterBrush"),
                    plotlyOutput("pokeScatter")
                ),
                box(
                    width=4,
                    title="Selected Pokemon",
                    wellPanel(
                        htmlOutput("sprite"),
                        tags$div(textOutput("pokeDescription"), class="wrapped")
                    )
                )
            ),
            box(
                width=12,
                title="Pokemon inside selection",
                DT::DTOutput("pokeTable")
            )
        ),
        tabItem(tabName="typeComparer",
                sidebarLayout(
                  sidebarPanel(
                    checkboxGroupInput("typePicker", "Choose types:",
                                       c("grass"="grass",
                                         "fire"="fire",
                                         "water"="water",
                                         "bug"="bug",
                                         "normal"="normal",
                                         "poison"="poison",
                                         "electric"="electric",
                                         "ground"="ground",
                                         "fairy"="fairy",
                                         "fighting"="fighting",
                                         "psychic"="psychic",
                                         "rock"="rock",
                                         "ghost"="ghost",
                                         "ice"="ice",
                                         "dragon"="dragon",
                                         "dark"="dark",
                                         "steel"="steel",
                                         "flying"="flying"))
                  ),
                  mainPanel(
                    plotlyOutput("linePlot")
                  )
              )
        )
    )
)))
